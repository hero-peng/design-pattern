package org.design.facade;


/*
* 外观模式Facade：
* 又叫作门面模式，是一种通过为多个复杂的子系统提供一个一致的接口，
* 而使这些子系统更加容易被访问的模式。该模式对外有一个统一接口，
* 外部应用程序不用关心内部子系统的具体细节，
* 这样会大大降低应用程序的复杂度，提高了程序的可维护性。
* 主要优点。
1、降低了子系统与客户端之间的耦合度，使得子系统的变化不会影响调用它的客户类。
2、对客户屏蔽了子系统组件，减少了客户处理的对象数目，并使得子系统使用起来更加容易。
3、降低了大型软件系统中的编译依赖性，简化了系统在不同平台之间的移植过程，因为编译一个子系统不会影响其
   他的子系统，也不会影响外观对象。
* */
public class FacadeMain {

    public static void main(String[] args) {

        FacadePattern facade = new FacadePattern();
        facade.method();

    }
}
